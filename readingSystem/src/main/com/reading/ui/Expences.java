    private void selectAllRecordCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectAllRecordCBActionPerformed
        // TODO add your handling code here:
        if(selectAllRecordCB.isSelected()){
            int rowCount = table.getRowCount();
            table.setRowSelectionInterval(0,rowCount-1);
        }else{
        	table.setRowSelectionInterval(0,0);
        }
    }//GEN-LAST:event_selectAllRecordCBActionPerformed

