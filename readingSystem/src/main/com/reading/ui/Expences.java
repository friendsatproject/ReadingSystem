    public void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        new AddExpenceHm(this);
        ((DefaultTableModel) table.getModel()).setRowCount(0);
        new SetAllTable().setExpenceTable(table);

    }// GEN-LAST:event_saveBtnActionPerformed

    private void calculateTotalBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_calculateTotalBtnActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row >= 0) {
            new FindTotalAmount(table, totalAmountTxt);
        } else {
            JOptionPane.showMessageDialog(null, "Please select records from table to calculate total amount.",
                    "Records not selected", JOptionPane.ERROR_MESSAGE);
        }
    }// GEN-LAST:event_calculateTotalBtnActionPerformed


